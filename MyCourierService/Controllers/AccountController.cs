        // POST: /Account/Register
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Register(RegisterViewModel model)
        {
            if (ModelState.IsValid)
            {
                var user = new ApplicationUser
                {
                    UserName = model.Email,
                    Email = model.Email,
                    Name = model.Name,       // Ensure these match your model
                    Surname = model.Surname, // Ensure these match your model
                    CreatedAt = DateTime.UtcNow,
                    WalletBalance = 10000,
                    IsActive = true
                };

                var result = await UserManager.CreateAsync(user, model.Password);

                if (result.Succeeded)
                {
                    // 1. Assign to Customer Role
                    await UserManager.AddToRoleAsync(user.Id, AppConstants.Roles.Customer);

                    // 2. Log them in
                    await SignInManager.SignInAsync(user, isPersistent: false, rememberBrowser: false);

                    // ==========================================
                    // NEW: SEND WELCOME EMAIL TO CUSTOMER
                    // ==========================================
                    try
                    {
                        await _emailService.SendCustomerWelcomeEmailAsync(user);
                    }
                    catch (Exception ex)
                    {
                        // We log the error but don't stop the user from using the site
                        System.Diagnostics.Debug.WriteLine("Customer Welcome Email Failed: " + ex.Message);
                    }
                    // ==========================================

                    return RedirectToAction("Index", "Customer");
                }
                AddErrors(result);
            }

            // If we got this far, something failed, redisplay form
            return View(model);
        }
