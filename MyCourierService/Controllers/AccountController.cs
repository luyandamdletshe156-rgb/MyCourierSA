        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string Role, string returnUrl)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            // 1. Attempt to sign in (Checks password and email)
            var result = await SignInManager.PasswordSignInAsync(model.Email, model.Password, model.RememberMe, shouldLockout: false);

            switch (result)
            {
                case SignInStatus.Success:
                    // 2. Fetch the user from the database
                    var user = await UserManager.FindByEmailAsync(model.Email);

                    // ==========================================
                    // NEW: DEACTIVATION SECURITY CHECK
                    // ==========================================
                    if (user != null && !user.IsActive)
                    {
                        // Even though the password was correct, the account is deactivated.
                        // We must sign them out immediately to clear the session cookie.
                        AuthenticationManager.SignOut(DefaultAuthenticationTypes.ApplicationCookie);
                        ModelState.AddModelError("", "Access Denied: This account has been deactivated by an administrator.");
                        return View(model);
                    }
                    // ==========================================

                    // 3. Fetch roles to check permissions
                    var actualRoles = await UserManager.GetRolesAsync(user.Id);

                    // 4. SECURITY CHECK: Does the user actually have the role they selected?
                    if (!actualRoles.Contains(Role))
                    {
                        AuthenticationManager.SignOut(DefaultAuthenticationTypes.ApplicationCookie);
                        ModelState.AddModelError("", $"This account does not have {Role} clearance.");
                        return View(model);
                    }

                    // 5. Redirect to the appropriate Dashboard
                    return await RedirectUserByRole(model.Email, returnUrl);

                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Invalid login attempt.");
                    return View(model);
            }
        }

