        // POST: /Account/ForgotPassword
        // POST: /Account/ForgotPassword
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> ForgotPassword(ForgotPasswordViewModel model)
        {
            if (ModelState.IsValid)
            {
                var user = await UserManager.FindByEmailAsync(model.Email);
                if (user == null)
                {
                    return View("ForgotPasswordConfirmation");
                }

                string code = await UserManager.GeneratePasswordResetTokenAsync(user.Id);
                var callbackUrl = Url.Action("ResetPassword", "Account",
                    new { userId = user.Id, code = code }, protocol: Request.Url.Scheme);

                // 3. Send the email using your existing ShipmentEmailService
                try
                {
                    // =============================================================
                    // THE FIX: Add 'await' and call the async version of the method
                    // =============================================================
                    await _emailService.SendPasswordResetEmailAsync(user.Email, callbackUrl);
                }
                catch (Exception ex)
                {
                    // Now that you are 'awaiting', this will properly catch the error
                    System.Diagnostics.Debug.WriteLine("Forgot Password Email Failed: " + ex.Message);
                    ModelState.AddModelError("", "There was a problem sending the email. Please try again later.");
                    return View(model);
                }

                return RedirectToAction("ForgotPasswordConfirmation", "Account");
            }

            return View(model);
        }
